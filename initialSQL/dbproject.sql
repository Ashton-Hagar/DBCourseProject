-- creates db in tool
create database hotels;

-- creates tables
create table hotelChain (
    hotelAmount int,
    officeAddress varchar(100),
    phone varchar(12),
    email varchar(50),
    chainName varchar(30) NOT NULL,
    PRIMARY KEY(officeAddress)
);

create table hotel (
    roomCount int,
    rating int,
    email varchar(50),
    phone varchar(12),
    chainName varchar(30),
    area varchar(30),
    addy varchar(100) NOT NULL, -- note: had to change address to addy because it was a datatype
    PRIMARY KEY(addy)
);

create table hotelRoom(
    price float,
    capacity int,
    viewType int, -- will code int to represent certain views
    extension int,
    problems varchar(100),
    addy varchar(100),
    roomNumber int NOT NULL,
    roomID int NOT NULL,
    PRIMARY KEY(roomID)
);

create table amenities(
        fridge int, -- all these ints are replacements for bool, where 0 = true, 1 = false
        TV int,
        AC int,
        microwave int,
        coffeeMaker int,
        roomID int NOT NULL,
        PRIMARY KEY(roomID)
);

create table customer(
    addy varchar(100),
    firstName varchar(20),
    lastName varchar(30),
    dateRegistered date,
    SSN int,
    customerID int NOT NULL,
    PRIMARY KEY(customerID)
);

create table manager(
    addy varchar(100),
    firstName varchar(20),
    lastName varchar(30),
    managerAddress varchar(100),
    SSN int,
    managerID int NOT NULL,
    PRIMARY KEY(managerID)
);

create table employee(
    empRole int, -- coded to display certain role for certain int
    firstName varchar(20),
    lastName varchar(30),
    empAddress varchar(100),
    managerID int,
    SSN int NOT NULL,
    PRIMARY KEY(SSN)
);

create table bookings(
    roomID int,
    customerID int,
    startDate date,
    endDate date,
    bookingID int,
    PRIMARY KEY(bookingID)
);

create table rentings(
    roomID int,
    customerID int,
    startDate date,
    endDate date,
    rentingID int,
    PRIMARY KEY(rentingID)
);

